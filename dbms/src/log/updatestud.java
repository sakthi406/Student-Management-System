    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        db.sid=jTextField1.getText();
        db.getStudent();
        try{
            jTextField2.setText(db.sid);
            jTextField3.setText(db.sname);
            jTextField4.setText(db.sdob);
            jTextField6.setText(db.sphone);
            jTextField9.setText(db.sage);
            jTextField7.setText(db.semail);
            jTextArea1.setText(db.sadd);
            if(db.sgen.equals("MALE")){
                jComboBox1.setSelectedIndex(0);
            }
            else if(db.sgen.equals("FEMALE")){
                jComboBox1.setSelectedIndex(1);
            }
            else if(db.sgen.equals("OTHERS")){
                jComboBox1.setSelectedIndex(2);
            }
        
        }catch(Exception e){
                //JOptionPane.showMessageDialog(null,e,"Alert",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

