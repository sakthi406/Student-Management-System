    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        db.sid=jTextField2.getText();
        db.sname=jTextField3.getText();
        db.sgen=String.valueOf(jComboBox1.getSelectedItem());
        db.sphone=jTextField6.getText();
        db.sdob=jTextField4.getText();
        db.sage=jTextField9.getText();
        db.semail=jTextField7.getText();
        db.sadd=jTextArea1.getText();
        if(jTextField4.getText().length()==0||jTextField3.getText().length()==0||jTextField6.getText().length()==0||jTextField9.getText().length()==0||jTextField7.getText().length()==0||jTextArea1.getText().length()==0){
            JOptionPane.showMessageDialog(this,"FIELDS SHOULD NOT BE LEFT EMPTY. PLEASE CHECK AGAIN.","Alert",JOptionPane.WARNING_MESSAGE);
        }
        else if(jTextField6.getText().length()!=10){
            JOptionPane.showMessageDialog(this,"Invalid phone number!!!","Alert",JOptionPane.WARNING_MESSAGE);
        }
        else{
            db.updatestudent();
            db.speak="Student updated successfully.";
            db.aivoice();
            JOptionPane.showMessageDialog(null,"Student Updated!!");
            
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField6.setText("");
            jTextField4.setText("");
            jTextField9.setText("");
            jTextField7.setText("");
            jTextArea1.setText("");
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

