    public showallstudentTeach() {
        db = new dbms();
        db.connect();
        
        initComponents();
        setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("icon.jpg")));
        try {
            String qur = "SELECT * FROM STUD_DETAILS;";
            db.st.execute(qur);
            db.r=db.st.getResultSet();
            DefaultTableModel mod = (DefaultTableModel)jTable1.getModel();
            while(db.r.next()){
                db.sid=db.r.getString("Reg_id");
                db.sname=db.r.getString("name");
                db.sgen=db.r.getString("gender");
                db.sdob=db.r.getString("dob");
                db.sphone=db.r.getString("phone");
                db.sage=db.r.getString("age");
                db.semail=db.r.getString("email");
                db.sadd=db.r.getString("address");
                
                mod.addRow(new Object[]{db.sid,db.sname,db.sgen,db.sdob,db.sphone,db.sage,db.semail,db.sadd});
                
            }
        }catch(Exception e){
            
        }
    }
